import {Mongo} from "meteor/mongo";

export const Web_Structure = new Mongo.Collection("web_structure");
Web_Structure.schema = new SimpleSchema({
    majorId: {
        type: String

    },
    order: {
        type: Number
    },
    name: {
        type: String
    },
    khName: {
        type: String
    },
    email: {
        type: String,
        optional: true
    },
    phoneNumber: {
        type: String,
        optional: true
    },
    title: {
        type: Object,
        optional: true,
        blackbox: true
    },
    body: {
        type: Object,
        optional: true,
        blackbox: true
    },
    url: {
        type: String,
        optional: true
    },

    branchId: {
        type: String
    },
    createdAt: {
        type: Date,
        optional: true,

        autoValue() {
            if (this.isInsert) {
                return moment().toDate();
            }
        }
    },
    updatedAt: {
        type: Date,
        optional: true,

        autoValue() {
            if (this.isUpdate) {
                return moment().toDate();
            }
        }
    },
    createdUser: {
        type: String,
        optional: true,

        autoValue() {
            if (this.isInsert) {
                return Meteor.userId();
            }
        }
    },
    updatedUser: {
        type: String,
        optional: true,
        autoValue() {
            if (this.isUpdate) {
                return Meteor.userId();
            }
        }
    }
});
Web_Structure.attachSchema(Web_Structure.schema);


