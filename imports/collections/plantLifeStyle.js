import {Mongo} from "meteor/mongo";

export const Web_PlantLifeStyle = new Mongo.Collection("web_plantLifeStyle");
Web_PlantLifeStyle.schema = new SimpleSchema({
    title: {
        type: Object,
        optional: true,
        blackbox: true
    },
    body: {
        type: Object,
        optional: true,
        blackbox: true
    },
    iframeLive: {
        type: String,
        optional: true
    },
    videoUrl: {
        type: String,
        optional: true
    },
    urlList: {
        type: [String],
        optional: true
    },
    addToHome: {
        type: Boolean,
        optional: true,
        defaultValue: false
    },
    type: {
        type: String,
        optional: true,
        defaultValue: "Plant Life Style"
    },
    branchId: {
        type: String
    },
    createdAt: {
        type: Date,
        optional: true,

        autoValue() {
            if (this.isInsert) {
                return moment().toDate();
            }
        }
    },
    updatedAt: {
        type: Date,
        optional: true,

        autoValue() {
            if (this.isUpdate) {
                return moment().toDate();
            }
        }
    },
    createdUser: {
        type: String,
        optional: true,

        autoValue() {
            if (this.isInsert) {
                return Meteor.userId();
            }
        }
    },
    updatedUser: {
        type: String,
        optional: true,
        autoValue() {
            if (this.isUpdate) {
                return Meteor.userId();
            }
        }
    }
});
Web_PlantLifeStyle.attachSchema(Web_PlantLifeStyle.schema);


