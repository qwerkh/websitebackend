import {Mongo} from "meteor/mongo";

export const Web_Organization = new Mongo.Collection("web_organization");
Web_Organization.schema = new SimpleSchema({
    body: {
        type: Object,
        optional: true,
        blackbox: true
    },
    url:{
        type:String,
        optional:true
    },
    branchId: {
        type: String
    },
    createdAt: {
        type: Date,
        optional: true,

        autoValue() {
            if (this.isInsert) {
                return moment().toDate();
            }
        }
    },
    updatedAt: {
        type: Date,
        optional: true,

        autoValue() {
            if (this.isUpdate) {
                return moment().toDate();
            }
        }
    },
    createdUser: {
        type: String,
        optional: true,

        autoValue() {
            if (this.isInsert) {
                return Meteor.userId();
            }
        }
    },
    updatedUser: {
        type: String,
        optional: true,
        autoValue() {
            if (this.isUpdate) {
                return Meteor.userId();
            }
        }
    }
});
Web_Organization.attachSchema(Web_Organization.schema);


