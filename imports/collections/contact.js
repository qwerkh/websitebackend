import {Mongo} from "meteor/mongo";

export const Web_Contact = new Mongo.Collection("web_contact");
Web_Contact.schema = new SimpleSchema({
    title: {
        type: Object,
        optional: true,
        blackbox: true
    },
    order: {
        type: Number,
        defaultValue: 1
    },
    address: {
        type: Object,
        optional: true,
        blackbox: true
    },
    phoneNumber: {
        type: String,
        optional: true
    },
    email: {
        type: String,
        optional: true
    },
    url: {
        type: String,
        optional: true
    },
    branchId: {
        type: String
    },
    createdAt: {
        type: Date,
        optional: true,

        autoValue() {
            if (this.isInsert) {
                return moment().toDate();
            }
        }
    },
    updatedAt: {
        type: Date,
        optional: true,

        autoValue() {
            if (this.isUpdate) {
                return moment().toDate();
            }
        }
    },
    createdUser: {
        type: String,
        optional: true,

        autoValue() {
            if (this.isInsert) {
                return Meteor.userId();
            }
        }
    },
    updatedUser: {
        type: String,
        optional: true,
        autoValue() {
            if (this.isUpdate) {
                return Meteor.userId();
            }
        }
    }
});
Web_Contact.attachSchema(Web_Contact.schema);


