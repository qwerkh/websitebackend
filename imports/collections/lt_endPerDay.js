import {Mongo} from "meteor/mongo";

export const LT_EndPerDay = new Mongo.Collection("lt_endPerDay");
LT_EndPerDay.schema = new SimpleSchema({
    closeDate: {
        type: String
    },
    time: {
        type: String,
        label: "Time"
    },
    agentId: {
        type: String,
        label: "Agent"
    }
    ,
    branchId: {
        type: String
    },
    createdAt: {
        type: Date,
        optional: true,

        autoValue() {
            if (this.isInsert) {
                return moment().toDate();
            }
        }
    },
    updatedAt: {
        type: Date,
        optional: true,

        autoValue() {
            if (this.isUpdate) {
                return moment().toDate();
            }
        }
    },
    createdUser: {
        type: String,
        optional: true,

        autoValue() {
            if (this.isInsert) {
                return Meteor.userId();
            }
        }
    },
    updatedUser: {
        type: String,
        optional: true,

        autoValue() {
            if (this.isUpdate) {
                return Meteor.userId();
            }
        }
    }
});
LT_EndPerDay.attachSchema(LT_EndPerDay.schema);


