import {Mongo} from "meteor/mongo";

export const LT_MapAgent = new Mongo.Collection("lt_mapAgent");
LT_MapAgent.schema = new SimpleSchema({
    agentList: {
        type: [String]
    },
    mainAgent: {
        type: String,
    },
    branchId: {
        type: String
    },
    createdAt: {
        type: Date,
        optional: true,

        autoValue() {
            if (this.isInsert) {
                return moment().toDate();
            }
        }
    },
    updatedAt: {
        type: Date,
        optional: true,

        autoValue() {
            if (this.isUpdate) {
                return moment().toDate();
            }
        }
    },
    createdUser: {
        type: String,
        optional: true,

        autoValue() {
            if (this.isInsert) {
                return Meteor.userId();
            }
        }
    },
    updatedUser: {
        type: String,
        optional: true,

        autoValue() {
            if (this.isUpdate) {
                return Meteor.userId();
            }
        }
    }
});
LT_MapAgent.attachSchema(LT_MapAgent.schema);


